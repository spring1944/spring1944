-- Let's append more factions as an extension
Spring.Log('icontypes', 'info', "Loading icons...")
local IconFiles = VFS.DirList("gamedata/icon_types", "*.lua")
Spring.Log('icontypes', 'info', "Found "..#IconFiles.." tables")
-- then add their contents to the main table
for _, IconFile in pairs(IconFiles) do
    Spring.Log('icontypes', 'info', " - Processing "..IconFile)
    local tmpTable = VFS.Include(IconFile)
    if tmpTable then
        for k,v in pairs(tmpTable) do
            iconTypes[k] = v
            Spring.Log('icontypes', 'info', " -- Added ".. k .." icon")
        end
        tmpTable = nil
    end
end

return iconTypes
